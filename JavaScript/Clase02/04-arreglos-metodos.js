const personas = []

/*
	El método "push" agrega un elemento al final de un arreglo
*/
personas.push("Sergio")
personas.push("Elder")
personas.push("José")

console.log(personas)

/*
	El método "unshift" agrega un elemento al comienzo de un arreglo
*/

personas.unshift("Raymi")
personas.unshift("Carol")
personas.unshift('Jana')

console.log(personas)

/*
	El método "pop" elimina el último elemento de un arreglo
*/
personas.pop()

console.log(personas)

/*
	El método "shift" elimina el primer elemento de un arreglo
*/
personas.shift()

console.log(personas)